        <section>
            <h3 class="">A equipe Scrum</h3>
            <h4>Product Owner (PO)</h4>
            <p>Representa os clientes e Stakeholders. É responsável por determinar as prioridades do Backlog do Produto. passa para a equipe tudo o que precisam saber para ter o máximo de valor</p>
            <h4>Scrum Master</h4>
            <p>O Scrum Master não tem poder sobre a equipe. Sua função é "deixar o caminho livre" para que a equipe desenvolvedora não encontre obstáculos. Além disso, eles são um elo entre a organização ou clientes e a Equipe Scrum como um todo. Além disso, ajuda o PO a descrever bem o Backlog e ajuda com o auto-gerenciamento da equipe.</p>
            <h4>Equipe de desenvolvimento</h4>
            <p>São croosfuncionais e auto-gerenciáveis. Transformam itens do backlog em funcionalidades de valor para o negócio.</p>
        </section>

        <section>
            <h3>Elementos do Scrum</h3>
            <h4>Product Backlog</h4>
            <p>Detalha tudo que deve ser necessário no produto. É desenvolvido "Just in time", ou seja: é detalhado apenas quando formos executá-lo. É ordenado levando em consideração o <span class="bold">tamanho</span>, <span class="bold">risco</span>, <span class="bold">valor</span> e <span class="bold">dependência</span>.</p>
            <h4>Sprint Backlog</h4>
            <p>Nele teremos o objetivo, os elementos do Product Backlog que irão para a Sprint e o plano a ser executado para alcançar o incremento.</p>
        </section>

        <section>
            <h3>Eventos do Scrum</h3>
            <h4>Sprint</h4>
            <p>Aquelas 2 a 4 semanas em que a equipe de desenvolvimento trabalha para entregar o que foi definido na sprint planning. Nela, o backlog pode ser refinado.</p>
            <h4>Sprint Planning</h4>
            <p>Descreve o trabalho a ser feito na Sprint.</p>
            <h4>Daily Scrum</h4>
            <p>Reunião diária de curta duração em que os devs falam o que fizeram no dia anterior e o que farão hoje para o Scrum Master.</p>
            <h4>Review</h4>
            <p>Aqui a equipe apresentará os frutos da Sprint. É um momento de feedback com o PO e stakeholders.</p>
            <h4>Retrospective</h4>
            <p>Reunião da equipe de desenvolvimento para discutir sobre como podemos melhorar a qualidade técnica e efetividade na próxima Sprint.</p>
        </section>

